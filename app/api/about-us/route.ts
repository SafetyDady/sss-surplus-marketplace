// POST/PUT - ‡∏≠‡∏±‡∏õ‡πÄ‡∏î‡∏ï‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏• About Us
export async function POST(request: NextRequest) {
  try {
    const body = await request.json();
    const { data, updatedBy = 'admin' } = body;
    
    const docRef = doc(db, 'aboutUs', 'companyInfo');
    
    // ‡πÄ‡∏û‡∏¥‡πà‡∏°‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏• timestamp ‡πÅ‡∏•‡∏∞‡∏ú‡∏π‡πâ‡∏≠‡∏±‡∏õ‡πÄ‡∏î‡∏ï
    const updateData = {
      ...data,
      lastUpdated: new Date().toISOString(),
      updatedBy: updatedBy
    };
    
    await updateDoc(docRef, updateData);
    
    return NextResponse.json({
      success: true,
      message: 'About us data updated successfully',
      data: updateData
    });
  } catch (error) {
    console.error('Error updating about us data:', error);
    return NextResponse.json({
      success: false,
      error: 'Failed to update about us data'
    }, { status: 500 });
  }
}

